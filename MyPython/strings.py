# indexing
greeting = 'hello'
print(len(greeting))
print(greeting[0])
print(greeting[3])
print(greeting[-1])
# i can use - value to take from back of the word

# slicing
print(greeting[2:])
# from index two to the end of the word

print(greeting[2:4])
# from 2index to 4index

print(greeting[-3: -1])
# also like those but from back

print(greeting[:3])
print(greeting[:])
# all symbols of word

print(greeting[::2])
# выскакиваем через одну букву

print(greeting[1::2])
# without 1st symbol

print(greeting[2::2])
print(greeting[::-1])
# to reverse word
